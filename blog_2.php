       <p>Pourquoi est-il important de faire des pauses durant sa journée de travail, quel que soit son métier ou ses fonctions ?<br></p>
       <p>Rester concentré sur son travail toute la journée, c’est comme rester en mouvement quand on est sportif de haut niveau. La comparaison est d’autant plus légitime qu’on sait que le cerveau est un muscle, d’ailleurs un gros consommateur de glucose. <br>

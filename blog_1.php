        <h2>Grâce aux micro-pauses améliorer vos performances au travail. </h2>

        <p>Nos méthodes de travail actuelles peuvent être épuisantes. Sous la pression du temps, nous avons du mal à prendre des pauses. Dans le cadre de la Chaire “Talents de la Transformation Digitale” les études démontrent que plusieurs types de micro-pauses peuvent améliorer la performance au travail. <br>
            <br>
         Les derniers travaux de notre cellule de recherches ont établi un lien entre les pauses favorisant <span>“l’errance mentale” </span> et la restauration de nos ressources attentionnelles.<br>
        <br>
         Dans la continuité de ces recherches, nous avons comparé les effets des micro-pauses avec et sans effort, pendant la réalisation d’une tâche soutenue. <br>
        300 personnes ont pris part à trois études réalisées dans un environnement contrôlé. Chaque groupe devait réaliser une tâche mentalement exigeante pendant 45 minutes avec, selon les groupes, une pause sans effort, une pause avec efforts, ou pas de pause du tout. <br>
        <br>
         Leur performance et la charge de travail perçue ont été mesurées tout au long de l’expérience. Dans le cas où le groupe n’avait pas de pause, les résultats indiquent une chute de la performance au fil du temps, de même pour le groupe qui bénéficiait de pauses requérant un effort mental. <br>
         Enfin, dans la condition où les personnes devaient prendre des pauses sans effort, la performance était maintenue durant tout le temps d’exécution de la tâche. <br>
          <br>
        Ainsi, 2 minutes de pause privilégiant « l’errance mentale » suffisent pour maintenir la performance des collaborateurs au travail. Aussi bien à l’école qu’au travail, cette solution peut être facilement mise en œuvre au cours de la journée, chaque fois que l’attention nécessite d’être conservée : pour maintenir sa concentration, améliorer l’apprentissage, contrer la fatigue ou lutter contre les effets de surcharge mentale. <br>
        Dans un tel contexte, il convient de proposer un support et une alternative afin d’aider les individus à optimiser leur temps et pouvoir réaliser des micro-pauses. <br> 
        L’application développée par Pause’reset est en ce sens, l’outil qui matérialise et aide ses utilisateurs à faire des micro-pause et conservent un niveau de concentration constant et élevé au travail. <br> </p>

